    private void list_historyMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_list_historyMouseClicked
        label_urlrequest.setText("URL request: ");
        String selected = list_history.getSelectedValue().toString();
        if (!selected.equals("Empty")){
            int index = list_history.getSelectedIndex();
            label_urlrequest.setText("URL: "+history.get(index).url);
            field_rawdata.setText(history.get(index).raw_response);
            
            if ( label_urlrequest.getText().equals("URL request: ")){
                label_urlrequest.setText("URL request not found");
            }
        }
    }//GEN-LAST:event_list_historyMouseClicked


