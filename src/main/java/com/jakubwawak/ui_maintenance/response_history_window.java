    /**
     * Function for loading window content
     */
    void load_window(){
        DefaultListModel dlm = new DefaultListModel();
        for(Request_History_Object rho : history){
            dlm.addElement(rho.show_glance());
        }
        if ( dlm.isEmpty() ){
            dlm.addElement("Empty");
        }
        list_history.setModel(dlm);
        field_rawdata.setEnabled(false);
    }

